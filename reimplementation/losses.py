import torch
from kornia.filters import SpatialGradient
from torch import nn


def image_and_light_loss(image_relit, image_gt, light_prediction, light_gt):
    img_l1 = torch.sum(torch.abs(image_relit - image_gt))

    image_relit_grad = SpatialGradient()(image_relit)
    image_gt_grad = SpatialGradient()(image_gt)
    grad_l1 = torch.sum(torch.abs(image_relit_grad - image_gt_grad))

    light_l2 = torch.sum((light_prediction - light_gt) ** 2)

    N = image_relit.shape[2]
    loss = ((img_l1 + grad_l1) / (N * N)) + (light_l2 / 9)
    return loss


def feature_loss(source_img_feature, target_img_feature):
    loss = torch.sum((source_img_feature - target_img_feature) ** 2)
    b, c, h, w = source_img_feature.shape
    n_f = (c * h * w)
    loss = loss / n_f
    return loss

